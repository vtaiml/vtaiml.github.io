<div class="grid grid-cols-2 gap-10 pt-5">
	<div class="col-span-1 pl-40">
		<h2 class="text-5xl text-violet-700 font-extrabold pb-5">coding</h2>
		<p class="text-2xl">
			While it's possible to learn theory alone, we highly recommend that you pick up coding if you want to implement your own models from scratch. 
            While many languages can be used, we recommend <strong>Python</strong>.
		</p>
        <br>
        <p class="text-2xl"><a href='https://codingbat.com/python'>CodingBat:</a> Site filled with live coding problems (Python/Java)</p>
        <p class="text-2xl"><a href='https://www.codecademy.com/learn/learn-python-3'>Codecademy:</a> Guided, self-paced online course</p>
        <p class="text-2xl"><a href='https://projecteuler.net/'>Project Euler:</a> More advanced problems, larger difficulty range</p>
        <br>
        <p class="text-2xl">As for frameworks, anything works. For general purposes, <strong>scikit-learn</strong> is very beginner-friendly. For more involved
            deep learning, we recommend <strong>PyTorch</strong>.
        </p>

        <h2 class="text-5xl text-violet-700 font-extrabold pb-5 pt-10">computation</h2>
		<p class="text-2xl">
			We recommend using <strong>Jupyter Notebook</strong> / <strong>Google Colab</strong>. Both allow you to run code/add markdown text
            in blocks, making it easy to combine code, comments, and visualizations.
            <br><br>
            As for GPUs, Virginia Tech has ARC (<a href="https://www.docs.arc.vt.edu/resources.html">Advanced Research Computing</a>), which hosts
            multiple clusters. The link show documentation for ARC's clusters, as well as info on how to access them. There are also some GPU nodes 
            on rlogin (use <code>'ssh portal.cs.vt.edu'</code>).
		</p>
	</div>
    <div class="col-span-1 pr-40">
		<h2 class="text-5xl text-violet-700 font-extrabold pb-5">textbooks/courses</h2>
		<p class="text-2xl">
			Andrew Ng has some online courses on <a href="https://www.coursera.org/learn/machine-learning">machine learning</a> and <a href="https://www.deeplearning.ai/">deep learning</a>.
            <br><br>
            Stanford has several useful resources. There's 
            <a href="https://cs229.stanford.edu/">CS229</a> (standard ML), 
            <a href="http://cs231n.stanford.edu/">CS231n</a> (Deep Learning for Computer Vision), 
            <a href="https://web.stanford.edu/class/cs234/">CS234</a> (Reinforcement Learning), 
            <a href="https://deepgenerativemodels.github.io/">CS236</a> (Generative Models), and much more.
            Definitely check out the lecture notes for these classes. Many have lectures videos online.
            <br><br>
            <a href="https://www.knowledgeisle.com/wp-content/uploads/2019/12/2-Aur%C3%A9lien-G%C3%A9ron-Hands-On-Machine-Learning-with-Scikit-Learn-Keras-and-Tensorflow_-Concepts-Tools-and-Techniques-to-Build-Intelligent-Systems-O%E2%80%99Reilly-Media-2019.pdf">
                This</a> is is a great textbook on machine learning if you prefer a hands-on approach.
            <a href="http://neuralnetworksanddeeplearning.com/">This</a> is a great online book on neural nets and deep learning.
            <a href="https://d2l.ai/">Here's</a> another great one. 
            Finally, <a href="https://mml-book.github.io/book/mml-book.pdf">this book</a> goes over several mathetmatical concepts in ML.
		</p>

